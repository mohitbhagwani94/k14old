				The word Konvolve has been derived from the word "convolution". Knowledge and learning is not restricting yourselves to your branch of specialization but about innovations and simple solutions to greater problems utilizing the knowledge of varied disciplines of learning.
Standing true to its meaning, the event KONVOLVE is an Intra-Srm Multidisciplinary Project Competition and Exhibition, which is open to students under all the faculties of SRM University. The event aims at bringing minds together, "convolving" from various disciplines, to come up with a project, based on their ideas, in the true spirit of learning.
Various projects in a number of versatile themes like Green Buildings, Intelligent Biology, Energy Efficient Machines and control, Green Vehicles etc. have been developed by the participants. 
 Initiated by the Associate Director (E&T), Dr. C Muthamizhchelvan in 2012, Konvolve draws in the development of various projects under a number of versatile themes like Green Buildings, Intelligent Biology, Energy Efficient Machines and control, Green Vehicles etc. by the participants. Special themes like ‘Technological solutions and Innovations at SRM Campus’ are also improvised by enthusiastic students. 
The shortlisted projects are honored with awards and medals. The best projects are funded by the University for Further Development.  Shortlisted candidates are also given an opportunity to attend internships in reputed companies.
In its first two editions, Konvolve experienced the presence of eminent personalities like Mr. Rajan Anandan, Managing director, Google India and Dr. Tessy Thomas, the Project Director of Agni-IV missile at DRDO for the prize distribution ceremonies.

