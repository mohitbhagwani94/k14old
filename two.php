<span style="font-size:30px">About Us</span><br><br>
The word Konvolve has been derived from the word "convolution". Knowledge and learning is not restricting yourselves to your branch of specialization but about innovations and simple solutions to greater problems utilizing the knowledge of varied disciplines of learning.<br/>
Standing true to its meaning, the event KONVOLVE is an Intra-Srm Multidisciplinary Project Competition and Exhibition, which is open to students under all the faculties of SRM University. The event aims at bringing minds together, "convolving" from various disciplines, to come up with a project, based on their ideas, in the true spirit of learning.<br/>
Various projects in a number of versatile themes like Green Buildings, Intelligent Biology, Energy Efficient Machines and control, Green Vehicles etc. have been developed by the participants. <br/>
Initiated by the Associate Director (E&T), Dr. C Muthamizhchelvan in 2012, Konvolve draws in the development of various projects under a number of versatile themes like Green Buildings, Intelligent Biology, Energy Efficient Machines and control, Green Vehicles etc. by the participants. Special themes like ‘Technological solutions and Innovations at SRM Campus’ are also improvised by enthusiastic students. <br/>
The shortlisted projects are honored with awards and medals. The best projects are funded by the University for Further Development.  Shortlisted candidates are also given an opportunity to attend internships in reputed companies.<br/>
In its first two editions, Konvolve experienced the presence of eminent personalities like Mr. Rajan Anandan, Managing director, Google India and Dr. Tessy Thomas, the Project Director of Agni-IV missile at DRDO for the prize distribution ceremonies.<br/><br/>

<h3>Konvolve'12:</h3>
Twenty three of the 110 multi-disciplinary projects registered were selected after three rounds of grueling scrutiny during the first edition of KONVOLVE, held from 16th to 18th Aug 2012 at the Kattankulathur campus of SRM University. The event was organized in association with AARUUSH, a national level techno-management fest.<br/>
<br/>
The event was conducted in three phases:<br/>
1) The Conceptual Phase (online), in which the participants are expected to send pre-project theory and literature,<br/>
2) The Design and Implementation Phase, in which the projects are judged by a panel of faculties from various departments on the said aspects.<br/>
3) The final round-The Testing and Evaluation Phase where the simulations/prototypes are evaluated, following which the selected teams should display their projects in a 2-day exhibition open to faculties and students. The exhibition was held again during AARUUSH '12.<br/>
<br/>
From the judging panel, Dr. R. Venkatesan, Radiological Safety Division, Indira Gandhi Centre for Atomic Research (IGCAR),<br/>
Kalpakkam highly appreciated the concept of initialising the concept of multi-disciplinary projects and also said that it should be encouraged and enhanced.<br/>
Backing The Director's idea of working models, Mr. Nageswara Rao, from Urjita Electronics Pvt. Ltd., urged the students to take it to a higher level by sending project proposals and approaching industries for research collaboration.<br/>
The best projects were highly appreciated at the prize distribution ceremony, wherein the participants received awards from Mr. Rajan Anandan, Managing Director at Google India.<br/>





<h3>Konvolve'13:</h3>
In continuation to the earlier edition, Konvolve'13 saw bigger and better projects that would create a greater impact in the society. The enthusiastic, innovative and creative young minds were continuously guided by the highly experienced faculty. The projects were judged on the basis of a 4-phase evaluation process.  Other than securing a funding opportunity for the best innovative ideas, the winning teams also had the scope to get internships in reputed companies. <br/>
Konvolve'13 experienced the presence of Dr. Tessy Thomas, project manager, Agni IV at DRDO for the prize distribution ceremony.<br/>
