<div align="center" style="font-size:35px;">Rules and Regulations</div>
<div style="background:rgba(0,0,0,0.7);padding:2%;">
<div class="tr_r">
A team must comprise a minimum of 3 members and a maximum of 15, in any combination of branches, faculties, year of study and SRM campuses, and a teaching Faculty Guide.
</div>
<div class="tr_r">
The complete event will comprise of 4 phases
</div>
<div class="tr_r">
First phase is the Conceptual Phase where the teams are supposed to present their concept based on their research in form of an abstract.
</div>
<div class="tr_r">
The abstract must not be of more than 200 words. The team name should be the heading of the abstract in font 14pt Times New Roman, followed by a name and contact of any one team member. The abstract should be in font 12pt Times New Roman. Highlighting of any part of text is not allowed
</div>
<div class="tr_r">
Second Phase is the Presentation Phase in which the shortlisted teams will present their concept and idea.
</div>
<div class="tr_r">
Third Phase is the Preliminary Design Review Phase in which the teams are supposed to display their prototype of their project. If a prototype is not feasible, simulation of the project can be exhibited.
</div>
<div class="tr_r">
Final Phase is the Exhibition Phase, in which the projects of the teams will be exhibited as well as evaluated by external panel of judges.
</div>
<div class="tr_r">
The rules for the second and third phases will be released only after the shortlist of the teams qualifying for the second phase is released.
</div>
<div class="tr_r">
The project can be based on any of the themes listed in the poster, which broadly represent various disciplines, but need not be limited to them.
</div>
<div class="tr_r">
The event is open to all the campus students and there is no restriction on the number of teams from any campus.
</div>
<div class="tr_r">
Projects will be evaluated based on originality, inter-disciplinary activity, practical and economic feasibility
</div>
<div class="tr_r">
The decision of the Executive Board will be final and binding.
</div>
